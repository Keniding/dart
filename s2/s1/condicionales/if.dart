void main() {
  // Sentencia de control y flujo

  int dia = 2;
  if(dia == 1) {
    print('El día es: lunes');
  }

  if(dia == 1) {
    print('El día es: lunes');
  } else {
    print('El día no es: lunes');
  }

  if(dia == 1) {
    print('El día es: lunes');
  } else if(dia == 2) {
    print('El día es: martes');
  } else if(dia == 3) {
    print('El día es: miercoles');
  } else if(dia == 4) {
    print('El día es: jueves');
  } else if(dia == 5) {
    print('El día es: viernes');
  } else {
    print('El día no es: lunes, martes, miercoles, jueves, viernes');
  }
}