void main() {
  // Variables bajamente tipadas
  // Son modificables
  dynamic valor = 'Henry';
  print('Este es el valor de ${valor}');

  valor = 30;
  print('Este es el valor de ${valor}');

  valor = false;
  print('Este es el valor de ${valor}');

  valor = 2.14;
  print('Este es el valor de ${valor}');
}